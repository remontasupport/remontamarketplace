export function searchAustralianLocations(query: string): AustralianLocation[] {
  if (!query || query.length < 1) return [];

  const searchTerm = query.toLowerCase().trim();

  return australianLocations
    .filter(location => {
      const lowerDisplay = location.display.toLowerCase();
      const lowerSuburb = location.suburb.toLowerCase();
      const lowerPostcode = location.postcode.toLowerCase();
      const lowerState = location.state.toLowerCase();

      return (
        lowerDisplay.includes(searchTerm) ||
        lowerSuburb.includes(searchTerm) ||
        lowerPostcode.includes(searchTerm) ||
        lowerState.includes(searchTerm) ||
        lowerPostcode.startsWith(searchTerm)
      );
    })
    .slice(0, 10) // Limit to 10 results for performance
    .sort((a, b) => {
      // Prioritize exact postcode matches
      if (a.postcode.startsWith(searchTerm) && !b.postcode.startsWith(searchTerm)) return -1;
      if (!a.postcode.startsWith(searchTerm) && b.postcode.startsWith(searchTerm)) return 1;

      // Then sort by suburb name
      return a.suburb.localeCompare(b.suburb);
    });
}